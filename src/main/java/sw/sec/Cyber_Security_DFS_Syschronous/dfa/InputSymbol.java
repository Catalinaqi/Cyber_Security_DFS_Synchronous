package sw.sec.Cyber_Security_DFS_Syschronous.dfa;

public enum InputSymbol {

    // Represents the actions a user can take during the login process
    ENTER_USERNAME,

    // Represents the action of entering a password
    ENTER_PASSWORD,

    // Represents the action of submitting credentials
    SUBMIT,

    // Represents a successful validation of credentials
    VALID_SUCCESS,

    // Represents a failed validation of credentials
    VALID_FAIL

}
